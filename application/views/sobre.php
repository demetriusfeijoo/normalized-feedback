            <p>
                  <strong>Normalized Feedback</strong> é um projeto que visa padronizar as mensagens de feedback retornadas nos diversos
                  sistemas espalhados pelo mundo afora. <br><br>

                  Sabemos que criar boas mensagens de feedback não é algo simples. Ela precisa ser curta, clara e amigável. <br>
                  Infelizmente, algumas vezes isso não acontece, o que causa para o usuário (e o suporte), algumas dores de cabeça.<br>

                  E se conseguíssemos padronizar essas mensagens e ao mesmo tempo torná-las genéricas?<br>
                  O usuário ganharia, pois ele já estaria acostumado com aquela mensagem.<br>
                  O desenvolvedor não gastaria muito tempo criando suas mensagens de feedback.<br>
                  E o patrão ficaria muito satisfeito com a qualidade da sua equipe e do seu software.               

